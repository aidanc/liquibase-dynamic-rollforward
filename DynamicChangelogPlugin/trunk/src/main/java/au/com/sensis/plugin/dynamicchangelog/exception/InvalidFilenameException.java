package au.com.sensis.plugin.dynamicchangelog.exception;

public class InvalidFilenameException extends DynamicChangelogException {

	private static final long serialVersionUID = 4603216296663207042L;

	public InvalidFilenameException() {
		super();
	}

	public InvalidFilenameException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidFilenameException(String arg0) {
		super(arg0);
	}

	public InvalidFilenameException(Throwable arg0) {
		super(arg0);
	}
}
